function isCatalogObject(value) {
  return Boolean(value) && typeof value === "object" && !Array.isArray(value);
}

function mergeCatalog(base, overrides) {
  return Object.fromEntries(
    Object.entries(base).map(([key, value]) => {
      const override = overrides?.[key];
      if (isCatalogObject(value)) {
        return [key, mergeCatalog(value, isCatalogObject(override) ? override : {})];
      }
      return [key, typeof override === "string" && override.trim() ? override : value];
    })
  );
}

function addLocaleCatalog(code, overrides) {
  messages[code] = mergeCatalog(messages.en, overrides);
}

